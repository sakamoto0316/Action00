#include "Sample.h"
#include "object2D.h"

//====================================================================
//コンストラクタ
//====================================================================
CSample::CSample()
{
	m_pSample2D = NULL;
}

//====================================================================
//デストラクタ
//====================================================================
CSample::~CSample()
{

}

//====================================================================
//サンプル一覧
//====================================================================
void CSample::Sample(void)
{
	//2Dポリゴンの設定
	m_pSample2D = CObject2D::Create();
	m_pSample2D->SetPos(D3DXVECTOR3(0.0f, 0.0f, 0.0f));
	m_pSample2D->SetWight(100.0f);
	m_pSample2D->SetHeight(100.0f);
	m_pSample2D->SetTexture("data\\TEXTURE\\0.png");


}